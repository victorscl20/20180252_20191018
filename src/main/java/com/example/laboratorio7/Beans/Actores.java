package com.example.laboratorio7.Beans;

public class Actores {
    private int id;
    private String nombre;
    private String apellido;
    private int n_categorias;
    private int n_peliculas;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public int getN_categorias() {
        return n_categorias;
    }

    public void setN_categorias(int n_categorias) {
        this.n_categorias = n_categorias;
    }

    public int getN_peliculas() {
        return n_peliculas;
    }

    public void setN_peliculas(int n_peliculas) {
        this.n_peliculas = n_peliculas;
    }
}
